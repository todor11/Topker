package com.topker.areas.brand.models.viewModels;

public class BrandNameViewModel {

    private Long id;

    private String name;

    public BrandNameViewModel() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
